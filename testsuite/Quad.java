public class Quad {
    private Point first = null;
    private Point second = null;
    private Point third = null;
    private Point fourth = null;

    public void setFirst(Point first) {
        this.first = first;
    }

    public void setSecond(Point second) {
        this.second = second;
    }

    public void setThird(Point third) {
        this.third = third;
    }

    public void setFourth(Point fourth) {
        this.fourth = fourth;
    }

    public Point getFirst() {
        return first;
    }

    public Point getSecond() {
        return second;
    }

    public Point getThird() {
        return third;
    }

    public Point getFourth() {
        return fourth;
    }
}
